-- =============================================================================
-- Интеграция PG Queue Balancer в существующую Lidogen_main_DB
-- =============================================================================
-- Назначение: накатить таблицы очереди в ТУ ЖЕ БД, где уже живёт
--   платформа лидогенерации (platforms, source_channels, source_messages, ...).
--
-- Отличие от DB/BD_schema.sql (greenfield):
--   * platforms и source_channels НЕ создаются (уже есть) — source_channels
--     только расширяется балансировочными полями (ТЗ §5.1, A8);
--   * используется существующая функция public.update_updated_at()
--     вместо set_updated_at() из BD_schema.sql;
--   * все объекты создаются идемпотентно (IF NOT EXISTS / DO-guard).
--
-- Применять ОДИН раз поверх дампа Lidogen_main_DB.sql:
--   psql "$QUEUE_DATABASE_URL" -f DB/A8_integrate_main_db.sql
-- Полная пересборка с нуля — см. docs(plan)/db-integration-plan.md, режим B.
-- =============================================================================

