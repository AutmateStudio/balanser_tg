  "id" bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "task_type_id" integer NOT NULL,
  "task_type_code" text NOT NULL,
  "status" task_status NOT NULL DEFAULT 'queued',
  "priority" integer NOT NULL,
