CREATE TABLE IF NOT EXISTS "task_types" (
  "id" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "code" text UNIQUE NOT NULL,
  "name" text NOT NULL,
  "description" text,
  "is_enabled" boolean NOT NULL DEFAULT false,
  "default_priority" integer NOT NULL,
  "min_available_resource_percent" integer NOT NULL,
  "requires_specific_account" boolean NOT NULL DEFAULT false,
  "uses_two_accounts" boolean NOT NULL DEFAULT false,
  "max_attempts" integer NOT NULL DEFAULT 5,
  "retry_delay_seconds" integer NOT NULL DEFAULT 10,
  "retry_backoff_multiplier" numeric NOT NULL DEFAULT 2,
  "max_retry_delay_seconds" integer NOT NULL DEFAULT 1800,
  "target_queue_size" integer,
  "max_postpone_count" integer NOT NULL DEFAULT 100,
