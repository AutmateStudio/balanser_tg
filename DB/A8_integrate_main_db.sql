-- --- 2. Таблицы очереди (source_channels/platforms НЕ трогаем) ------------------
CREATE TABLE IF NOT EXISTS "resource_op_types" (
  "id" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "code" text UNIQUE NOT NULL,
  "name" text,
  "rph_limit" integer NOT NULL,
  "reserve_percent" numeric NOT NULL DEFAULT 10,
